package org.project_printing_shop.enums;

/**
 * Enum representing different sizes of paper used in the printing shop.
 * The sizes are standardized as A1, A2, A3, A4, and A5.
 */
public enum PaperSize {
    A1,  // Largest paper size in this enumeration
    A2,  // Second-largest paper size
    A3,  // Medium paper size
    A4,  // Commonly used paper size for standard documents
    A5   // Smallest paper size in this enumeration
}