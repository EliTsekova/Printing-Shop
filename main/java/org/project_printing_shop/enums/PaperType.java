package org.project_printing_shop.enums;

/**
 * Enum class representing different types of paper used in the printing shop.
 * The paper types include STANDARD, GLOSSY, and NEWS_PRINT.
 */
public enum PaperType {
    STANDARD,  // Regular paper type used for most printing needs
    GLOSSY,    // Glossy paper type used for high-quality prints, such as magazines and photos
    NEWS_PRINT // Paper type specifically used for printing newspapers
}